package Chap06.h_class_p232;

public class ha_Calculator {
	//정사각형의 넓이
	double areaRectangle(double width) {
		return width * width;
	}
	//직사각형 넓이
	double areaRectangle(double width, double height) {
		return width * height;
	}
		//직사각형 넓이
		//선생님 예 숫자뒤에 .0 없애래
	int areaRectangle(int width, int height) {
		return width * height;

	}

}
