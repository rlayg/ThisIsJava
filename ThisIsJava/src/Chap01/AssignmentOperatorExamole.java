package Chap01;

public class AssignmentOperatorExamole {
	public static void main(String[] args) {
		int result = 0;
			
		System.out.println("result= " + result);
		result -=3; // result = result - 3
		System.out.println("result= " + result);
		result *=2;
		System.out.println("result= " + result);
		result /=2;
		System.out.println("result= " + result);
		result %=3;
		System.out.println("result= " + result);
	}   
	// 이것이 자바다 p.100
}
